package assignment;

public class OperationEx4 {

	public static void main(String[] args) {
		
		//복합 대입 연산
		int num = 10;
		System.out.println(num+=1);
		
		System.out.println(num%=10);
		
		num-=1;
		System.out.println(num);
		
		//조건 연산자
		int num1 = (5 > 3) ? 10 : 20;
		System.out.println(num1);
		
	}

}
